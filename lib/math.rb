def addition(num1, num2)
  sum = num1 + num2
  sum 
end 

def subtraction(num1, num2)
  diff = num1 - num2
  diff
end 

def multiplication(num1, num2)
  prod = num1 * num2
  prod
end 

def division(num1, num2)
  quot = num1 / num2
  quot
end 

def modulo(num1, num2)
  mod = num1 % num2
  mod
end 

def square_root(num)
  Math.sqrt(num) 
  num 
end 
